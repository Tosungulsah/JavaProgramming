package day09_IfStatement;

public class PosNegZero {
    public static void main(String[] args) {
        int a = 0 ;

        if (a< 0){
            System.out.println(a+ " is negative");
        }
        else if(a>0){
            System.out.println(a+ "is a positive number");
        }
        else {
            System.out.println(a+ " is zero");
        }



    }
}
