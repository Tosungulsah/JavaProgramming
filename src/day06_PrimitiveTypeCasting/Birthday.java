package day06_PrimitiveTypeCasting;

public class Birthday {
    public static void main(String[] args) {
        String name= "Gulsah",
             birthMonth = "January";

        int birthDay= 22,
             birthYear= 1987;

        System.out.println(name+ " was born on "+ birthMonth+ "/"+birthDay+ "/"+birthYear+"." );

    }
}
