package day08_IfStatement;

public class IfStatement {
    public static void main(String[] args) {

        int n= 1200;
        // positive
        if(n>0){
            System.out.println(n + " is positive");
        }
        //negative
        if(n<0){
            System.out.println(n + " is negative");
        }
        //  zero
        if(n==0) {
            System.out.println(n + " is 0");
        }


    }
}
