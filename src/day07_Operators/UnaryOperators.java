package day07_Operators;

public class UnaryOperators {

    public static void main(String[] args) {
        /*
pre and post are different
        */

        int z = 45;
        System.out.println(++z);
        System.out.println(z++);
        System.out.println(z);
        System.out.println("*************************************************");

        int b= 4;

        ++b;
        ++b;
        ++b;

        System.out.println(b);



    }

}
